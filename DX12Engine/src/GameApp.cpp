    gridRitem->StartIndexLocation = gridRitem->Geo->DrawArgs["grid"].StartIndexLocation;
    gridRitem->BaseVertexLocation = gridRitem->Geo->DrawArgs["grid"].BaseVertexLocation;

    auto boxRitem = std::make_unique<RenderItem>();
    XMStoreFloat4x4(&boxRitem->World, XMMatrixTranslation(3.0f, 2.0f, -9.0f));
    boxRitem->ObjCBIndex = 1;
    boxRitem->Mat = mMaterials["wirefence"].get();
    boxRitem->Geo = mGeometries["boxGeo"].get();
    boxRitem->PrimitiveType = D3D11_PRIMITIVE_TOPOLOGY_TRIANGLELIST;
    boxRitem->IndexCount = boxRitem->Geo->DrawArgs["box"].IndexCount;
    boxRitem->StartIndexLocation = boxRitem->Geo->DrawArgs["box"].StartIndexLocation;
    boxRitem->BaseVertexLocation = boxRitem->Geo->DrawArgs["box"].BaseVertexLocation;

    auto quadRitem = std::make_unique<RenderItem>();
    XMStoreFloat4x4(&quadRitem->World, XMMatrixTranslation(3.0f, 5.0f, -9.0f));
    quadRitem->ObjCBIndex = 2;
    quadRitem->Mat = mMaterials["water"].get();
    quadRitem->Geo = mGeometries["quadGeo"].get();
    quadRitem->PrimitiveType = D3D11_PRIMITIVE_TOPOLOGY_TRIANGLELIST;
    quadRitem->IndexCount = quadRitem->Geo->SubMeshes[0].IndexCount;
    quadRitem->StartIndexLocation = quadRitem->Geo->SubMeshes[0].StartIndexLocation;
